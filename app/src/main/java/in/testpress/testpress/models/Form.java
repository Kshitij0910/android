package in.testpress.testpress.models;

public class Form {

    private String formId;
    private String formName;
    private String status;
    private String statusmsg;
    private String statusbgcolor;
    private String descbgcolor;
    private String desccolor;
    private String desc;

    public String getFormId() {
        return formId;
    }

    public void setFormId(String formId) {
        this.formId = formId;
    }

    public String getFormName() {
        return formName;
    }

    public void setFormName(String formName) {
        this.formName = formName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatusmsg() {
        return statusmsg;
    }

    public void setStatusmsg(String statusmsg) {
        this.statusmsg = statusmsg;
    }

    public String getStatusbgcolor() {
        return statusbgcolor;
    }

    public void setStatusbgcolor(String statusbgcolor) {
        this.statusbgcolor = statusbgcolor;
    }

    public String getDescbgcolor() {
        return descbgcolor;
    }

    public void setDescbgcolor(String descbgcolor) {
        this.descbgcolor = descbgcolor;
    }

    public String getDesccolor() {
        return desccolor;
    }

    public void setDesccolor(String desccolor) {
        this.desccolor = desccolor;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

}
